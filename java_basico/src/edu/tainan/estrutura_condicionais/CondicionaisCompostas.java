package edu.tainan.estrutura_condicionais;

public class CondicionaisCompostas {
    public static void main(String[] args) {

        // Variaveis
        int nota = 8;

        // Condição composta(se e senão)
        if (nota >= 7) {
            System.out.println("Aprovado");
        }
        else {
            System.out.println("Reprovado");
        } 
    }
}
