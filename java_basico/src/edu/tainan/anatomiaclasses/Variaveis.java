package edu.tainan.anatomiaclasses;

public class Variaveis {
    public static void main (String [] args) {
        String nome = "Tainan"; // Declarando váriavel.
        int idade = 24; // Declarando váriavel.
        final String BR = "Brasil"; // Variavel constante. final defini a variavel como constante
        System.out.print(BR);
        System.out.print(nome);
        System.out.print(idade);
    }
}


